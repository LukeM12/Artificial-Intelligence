

class NodeEvent:
    def __init__(time, date, lookTime, kindOfLook, personNames):
        self.time = time
        self.date = date
        self.ltime = lookTime
        self.kind = kindOfLook
        self.names = personNames

    def getDate():
        return self.date
    def getTime():
        return self.kind
    def getLookTime():
        return self.ltime
    def getLookKind():
        return self.kind
